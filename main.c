SAMPLE * importeSon(char *nomDeFichier)
{
    SAMPLE *sonARendre= load_sample(nomDeFichier);
    if(!sonARendre)
    {
        allegro_message("ne peut pas ouvrir %s",nomDeFichier);
        allegro_exit();
        exit(EXIT_FAILURE);
    }
    return sonARendre;
}


BITMAP * importeImage(char *nomDeFichier)
{
    BITMAP *imageARendre= load_bitmap(nomDeFichier,NULL);
    if(!imageARendre)
    {
        allegro_message("ne peut pas ouvrir %s",nomDeFichier);
        allegro_exit();
        exit(EXIT_FAILURE);
    }
    return imageARendre;
}

void drawTile(BITMAP*buffer, BITMAP *image, int x, int y)
{
    x_screen = x_start + (x - y) * TILE_WIDTH/2;
    y_screen = y_start + (x + y) * TILE_HEIGHT/4;
    draw_sprite(buffer,image,x_screen,y_screen);
}

void drawMap(BITMAP*buffer, BITMAP **image)
{
    for (int i = 0; i < GRID_SIZE; i++) {
        for (int j = 0; j < GRID_SIZE; j++) {
            if(mapF[i*GRID_SIZE+j]>-1)
            {
                drawTile(buffer,image[mapF[i*GRID_SIZE+j]], i, j);
            }

        }
    }
}

void button()
{
    if(key[KEY_UP])
    {
        y_start++;
    }
    if(key[KEY_DOWN])
    {
        y_start--;
    }
    if(key[KEY_LEFT])
    {
        x_start++;
    }
    if(key[KEY_RIGHT])
    {
        x_start--;
    }
}

void multiplyVector(Vector2D xTransfo,Vector2D yTransfo,int *x,int *y)
{
    *x=xTransfo.x*(*x)+yTransfo.x*(*y);
    *y=xTransfo.y*(*x)+yTransfo.y*(*y);
}


